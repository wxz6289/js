!function() {
    console.log("foo");
}();

/*
> `!function(){}()` 中的 `!` 本质上是逻辑非运算符，但它更重要的作用是**把匿名函数从函数声明转换为函数表达式**。JavaScript 规定，匿名函数声明是非法的，而函数表达式可以立即调用。由于一元运算符 `!` 后面必须是一个表达式，因此解析器会把 `function(){}` 按函数表达式处理，整个代码等价于 `!(function(){}())`。函数先执行，再对返回值进行逻辑非运算。实际上，除了 `!`，`+`、`-`、`~`、`void` 或外层括号 `()` 都可以达到将函数声明转换为函数表达式的目的，其中 `(function(){})()` 是目前最常见、可读性最好的 IIFE 写法。
*/
